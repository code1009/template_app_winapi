#include "pch.hpp"
#include "MsgLoop.hpp"





////////////////////////////////////////////////////////////////////////////////
//==============================================================================
MsgLoop::MsgLoop()
{
}

//==============================================================================
MsgLoop::~MsgLoop()
{
}

//==============================================================================
void MsgLoop::runMsgLoop(void)
{
	//-----------------------------------------------------------------------
	MSG msg;
	BOOL rv;


	do
	{
		rv = ::PeekMessageW(&msg, HWND{}, 0, 0, PM_REMOVE);
		if (rv != 0)
		{
			::TranslateMessage(&msg);
			::DispatchMessageW(&msg);
		}
	} while (msg.message != WM_QUIT);
}
